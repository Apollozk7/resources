-- functions

ui.getRandomPoints=function(points)
    local p={}
    local s={}
    while(#p < points)do
        local rnd=math.random(1,#ui.points)
        if(not s[rnd])then
            s[rnd]=true
            p[#p+1]=ui.points[rnd]
        end
    end

    s={}
    for i,v in pairs(p) do
        for i,v in pairs(v) do
            v[4]=math.random(1,2)
            s[#s+1]=v
        end
    end

    return s
end

